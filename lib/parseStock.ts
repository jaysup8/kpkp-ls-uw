export type ParseResult = {
  rawName: string
  rawValue: string
  itemId: string | null
  matchedName: string | null  // Thai name of matched item
  closing: number | null      // null = no update, 0 = หมด, number = value
}

// Normalize Thai text for matching: remove spaces, parentheses, noise
function norm(s: string): string {
  return s
    .replace(/\s+/g, '')
    .replace(/[()（）【】\[\]]/g, '')
    .replace(/พลิก/g, 'พริก')   // common typo: พลิก → พริก
    .replace(/ชอส/g, 'ซอส')    // common typo: ชอส → ซอส
    .replace(/เเ/g, 'แ')        // double เ+แ → แ
    .toLowerCase()
}

function matches(input: string, alias: string): boolean {
  const ni = norm(input)
  const na = norm(alias)
  if (ni === na) return true
  if (ni.length >= 3 && na.includes(ni)) return true
  if (na.length >= 3 && ni.includes(na)) return true
  return false
}

