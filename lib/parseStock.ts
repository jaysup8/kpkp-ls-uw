export function findItemId(name: string): { itemId: string; matchedName: string } | null {
  for (const [aliases, itemId, matchedName] of ALIAS_MAP) {
    for (const alias of aliases) {
      if (matches(name, alias)) return { itemId, matchedName }
    }
  }
  return null
}

export function parseClosingValue(raw: string): number | null {
  const t = raw.trim()
  if (!t) return null  // empty → no update
  if (/หมด|0$/.test(t)) return 0
  const m = t.match(/(\d+(?:[.,]\d+)?)/)
  if (m) return parseFloat(m[1].replace(',', '.'))
  return null  // can't parse → no update
}

export function parseStockText(text: string): ParseResult[] {
  return text
    .split('\n')
    .map(l => l.trim())
    .filter(l => l.includes(':'))
    .map(line => {
      const idx = line.indexOf(':')
      const rawName = line.slice(0, idx).trim()
      const rawValue = line.slice(idx + 1).trim()
      const match = findItemId(rawName)
      return {
        rawName,
        rawValue,
        itemId: match?.itemId ?? null,
        matchedName: match?.matchedName ?? null,
        closing: parseClosingValue(rawValue),
      }
    })
    .filter(r => r.rawName.length > 0)
}
